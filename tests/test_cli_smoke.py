class TestJSONOutput:
    """`--json` flag for machine-readable agent integration."""

    def test_translate_md_json(self, opp_intermediates, tmp_path):
        out = tmp_path / "md_json"
        proc = run_ol(
            "translate-md", str(opp_intermediates["md"]),
            "-o", str(out),
            "-s", "en", "-t", "zh",
            "--json",
        )
        assert proc.returncode == 0, f"stderr: {proc.stderr[-1000:]}"
        import json as _json
        # Find a JSON line in stdout.
        for line in proc.stdout.splitlines():
            line = line.strip()
            if line.startswith("{"):
                obj = _json.loads(line)
                assert obj.get("success") is True, f"json: {obj}"
                assert obj.get("output_file"), f"missing output_file: {obj}"
                return
        pytest.fail(f"no JSON object found in stdout:\n{proc.stdout[:1000]}")
