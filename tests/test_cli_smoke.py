"""OL CLI smoke: invoke `translate-md` and `translate-xliff` end-to-end.

Spawns the OL CLI as a real subprocess (not in-process CliRunner) so we
exercise the same code path users hit from the shell. The LLM call is
intercepted via OMNI_TEST_FAKE_LLM=1, which OL's own _translate_md_async /
_translate_xliff_async short-circuit handlers honour.

Inputs are generated by running the OPP CLI first (using a real DOCX
fixture) so we test the actual user workflow:

    OPP --target-format=both <docx> -o <intermediate_dir>/
        -> intermediate_dir/<docx>.md
        -> intermediate_dir/<docx>.xlf
        -> intermediate_dir/<docx>_manifest.json
        -> intermediate_dir/<docx>.skeleton.zip

    ol translate-md  <intermediate_dir>/<docx>.md   -o <out>/
    ol translate-xliff <intermediate_dir>/<docx>.xlf -o <out>/

Run with:
    cd /mnt/d/贯维/Omni_Suite/Omni_Localizer
    OMNI_TEST_FAKE_LLM=1 pytest tests/test_cli_smoke.py -v --tb=short
"""
from __future__ import annotations

import os
import shutil
import subprocess
from pathlib import Path

import pytest


VENV_PY = "/mnt/d/贯维/Omni_Suite/.venv_ol/bin/python"
OL_DIR = Path("/mnt/d/贯维/Omni_Suite/Omni_Localizer")
OPP_DIR = Path("/mnt/d/贯维/Omni_Suite/Omni_Pre_Processor")
REPO_ROOT = Path("/mnt/d/贯维/Omni_Suite")

HAIER_DOCX = REPO_ROOT / "爱上海尔_第二章_全球创牌 - E2E测试专用.docx"
MERIDIAN_DOCX = REPO_ROOT / "Meridian_Robotics_Product_Overview_E2E.docx"

_BASE_ENV = {
    **os.environ,
    "OMNI_TEST_FAKE_LLM": "1",
    "PYTHONUNBUFFERED": "1",
    "PYTHONIOENCODING": "utf-8",
}


def run_ol(*args: str, timeout: int = 240) -> subprocess.CompletedProcess:
    """Invoke `python -m ol_cli ...` in OL_DIR with the venv interpreter."""
    cmd = [VENV_PY, "-m", "ol_cli", *args]
    return subprocess.run(
        cmd,
        cwd=str(OL_DIR),
        capture_output=True,
        text=True,
        timeout=timeout,
        env=_BASE_ENV,
    )


def run_opp_extract(docx: Path, out_dir: Path, target_format: str = "both") -> None:
    """Run OPP CLI to produce MD/XLIFF intermediates for `docx` in `out_dir`."""
    cmd = [
        VENV_PY, "-m", "opp.cli",
        str(docx),
        "--target-format", target_format,
        "--source-lang", "en", "--target-lang", "zh",
        "--output-dir", str(out_dir),
    ]
    proc = subprocess.run(
        cmd,
        cwd=str(OPP_DIR),
        capture_output=True,
        text=True,
        timeout=240,
        env=_BASE_ENV,
    )
    assert proc.returncode == 0, (
        f"OPP pre-extract failed: exit={proc.returncode}\n"
        f"stderr(last 1500)={proc.stderr[-1500:]}"
    )


@pytest.fixture(scope="module", autouse=True)
def _check_real_fixtures():
    for p in (HAIER_DOCX, MERIDIAN_DOCX):
        if not p.exists():
            pytest.skip(f"Real fixture missing: {p}")


@pytest.fixture(scope="module")
def opp_intermediates(tmp_path_factory) -> dict[str, Path]:
    """Run OPP once on a real DOCX; reuse the MD/XLIFF for every test."""
    out = tmp_path_factory.mktemp("ol_opp_intermediates")
