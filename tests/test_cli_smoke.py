class TestTranslateMD:
    """`ol translate-md <input.md> -o <out>/` end-to-end."""

    def test_translate_md_produces_output(self, opp_intermediates, tmp_path):
        out = tmp_path / "md_out"
        proc = run_ol(
            "translate-md", str(opp_intermediates["md"]),
            "-o", str(out),
            "-s", "en", "-t", "zh",
        )
        assert proc.returncode == 0, (
            f"exit={proc.returncode}\n"
            f"stdout(last 1000)={proc.stdout[-1000:]}\n"
            f"stderr(last 1500)={proc.stderr[-1500:]}"
        )
        # The translated file is written under <out>/<basename>.md
        out_files = list(out.glob("*.md"))
        assert out_files, f"no .md produced in {out} (found: {list(out.iterdir())})"
        translated = out_files[0]
        content = translated.read_text(encoding="utf-8")
        # OL adds a YAML frontmatter with OL provenance.
        assert content.startswith("---"), f"no frontmatter in:\n{content[:200]}"
        assert "processor:" in content
        assert '"OL"' in content
        # Body should be non-empty.
        body = content.split("---", 2)[-1].strip()
        assert len(body) > 0, "translated body is empty"

    def test_translate_md_explicit_output_dir(self, opp_intermediates, tmp_path):
        """Document the -o / --output-dir override path with an explicit value.

        Note: OL's translate-md option is declared as
        `typer.Option("--output-dir", "-o", ...)`, which makes the literal
        string "--output-dir" the *default* value. Without -o, OL still runs
        and writes to a literal directory called "--output-dir/" — that
        surprising behavior is captured here by always passing an explicit
        -o, so we test the intended workflow.
        """
        out = tmp_path / "explicit_out"
        proc = run_ol(
            "translate-md", str(opp_intermediates["md"]),
            "-o", str(out),
            "-s", "en", "-t", "zh",
        )
        assert proc.returncode == 0, f"stderr: {proc.stderr[-1000:]}"
        assert out.exists()
        assert any(out.glob("*.md"))


class TestTranslateXLIFF:
    """`ol translate-xliff <input.xlf> -o <out>/` end-to-end."""

    def test_translate_xliff_produces_output(self, opp_intermediates, tmp_path):
        out = tmp_path / "xlf_out"
        proc = run_ol(
            "translate-xliff", str(opp_intermediates["xlf"]),
            "-o", str(out),
            "-s", "en", "-t", "zh",
        )
        assert proc.returncode == 0, (
            f"exit={proc.returncode}\n"
            f"stdout(last 1000)={proc.stdout[-1000:]}\n"
            f"stderr(last 1500)={proc.stderr[-1500:]}"
        )
        out_files = list(out.glob("*.xlf"))
        assert out_files, f"no .xlf produced in {out} (found: {list(out.iterdir())})"
        translated = out_files[0]
        content = translated.read_text(encoding="utf-8")
        # Valid XLIFF, with targets filled.
        assert "<xliff" in content
        assert "<target" in content
        # Real translated text > 0 — the OPP XLIFF has empty <target>s;
        # OL fills them.
        # Find at least one <target> with content.
        import re
        targets = re.findall(r"<target[^>]*>([^<]*)</target>", content)
        non_empty = [t.strip() for t in targets if t.strip()]
        assert non_empty, (
            f"no <target> with content filled — XLIFF:\n{content[:1000]}"
        )

    def test_translate_xliff_preserves_source(self, opp_intermediates, tmp_path):
        """The original XLIFF <source> text should survive translation."""
        out = tmp_path / "xlf_preserve"
        proc = run_ol(
            "translate-xliff", str(opp_intermediates["xlf"]),
            "-o", str(out),
            "-s", "en", "-t", "zh",
        )
        assert proc.returncode == 0
        original = opp_intermediates["xlf"].read_text(encoding="utf-8")
        translated = list(out.glob("*.xlf"))[0].read_text(encoding="utf-8")
        # Compare source-text fragments (rough sanity)
        import re
        orig_sources = re.findall(r"<source>([^<]*)</source>", original)
        trans_sources = re.findall(r"<source>([^<]*)</source>", translated)
        assert orig_sources, "no <source> in original XLIFF"
        assert orig_sources == trans_sources, (
            "OL must not modify <source> elements"
        )


