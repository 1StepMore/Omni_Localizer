    assert md.exists() and md.stat().st_size > 0, f"no MD at {md}"
    assert xlf.exists() and xlf.stat().st_size > 0, f"no XLF at {xlf}"
    return {"md": md, "xlf": xlf, "dir": out, "stem": stem}


